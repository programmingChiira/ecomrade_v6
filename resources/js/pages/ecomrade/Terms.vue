import Footer from './Footer.vue'

export default {

  components: {
    Footer
  },
  emits: ["updateSidebar"],
  data() {
    return {
      posts: [],
      fields: {
        //
      },
      loading: true,
    };
  },

  methods: {

    submit() {
      const fd = new FormData();
      fd.append("name", this.fields.name);
      fd.append("contact", this.fields.contact);
      fd.append("feedback", this.fields.feedback);

      fd.append("_method", "POST");

      axios
        .post("/api/feeds", fd, {
          headers: { "content-type": "multipart/form-data" },
        })
        .then(() => {
          this.fields = {};
          this.success = true;
          this.errors = {};

          Swal.fire({
            position: 'top-end',
            icon: 'success',
            title: 'Feedback <strong> Sent</strong>',
            showConfirmButton: false,
            timer: 1500
          })


          setTimeout(() => {
            this.success = false;
          }, 2500);
        })
        .catch((error) => {
          this.errors = error.response.data.errors;
          this.success = false;
        });
    },

  },

  mounted() {
    setTimeout(() => {
      this.loading = false;
    }, 2000);

    axios
      .get("/api/home-posts")
      .then((response) => (this.posts = response.data.data))
      .catch((error) => {
        console.log(error);
      });
  },
};
